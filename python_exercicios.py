# Números pares e ímpares: Escreva um programa que peça ao usuário para inserir um número e, em seguida, informe se o número é par ou ímpar.

# python

num = int(input("Digite um número: "))
if num % 2 == 0:
    print("O número é par.")
else:
    print("O número é ímpar.")



